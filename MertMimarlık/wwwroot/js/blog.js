// JavaScript ile "read more" işlevselliğini ekleyin
document.addEventListener('DOMContentLoaded', function () {
    const button = document.querySelector('.read-more-button1');
    const hiddenContent = document.querySelector('.hidden1');

    button.addEventListener('click', function () {
        if (hiddenContent.style.display === 'none' || hiddenContent.style.display === '') {
            hiddenContent.style.display = 'block';
            button.textContent = 'içeriği kapat';
        } else {
            hiddenContent.style.display = 'none';
            button.textContent = 'DEVAMINI OKU';
        }
    });
});

